namespace MASFoundation
{
    /// <summary>
    /// Logging level
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Do not log any errors
        /// </summary>
        None = 0,

        /// <summary>
        /// Log everything
        /// </summary>
        Full,

        /// <summary>
        /// Log warnings and errors only
        /// </summary>
        ErrorOnly
    }
}
